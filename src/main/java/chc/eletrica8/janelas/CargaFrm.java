    public void iniciaTabelaCargas(String dica) {
        List<Carga> lista = new ArrayList<>();
        if (Ids.getIdCircuito() == 0 || dica.equals("Novo")) {
            lista = CargaService.getByExpres("from Carga where circuito = null", null);
        } else if (dica.equals("Copia") || dica.equals("Exclui") || dica.equals("Salva") || dica.equals("")) {
            lista = CargaService.getByExpres("from Carga where circuito = :ID", new Object[]{"ID", CircuitoService.getById(Ids.getIdCircuito())});
        }
        if (!lista.isEmpty()) {
            tabelaModeloCarga = new CargaTableModel(lista);
            tabelaEquipamento.setModel(tabelaModeloCarga);
        } else {
            DefaultTableModel model = new DefaultTableModel();
            this.tabelaEquipamento.setModel(model);
        }
        Ids.imprimiIds();
    }

    private Carga getDados() {
        Carga carga;
        if (Ids.getIdCarga() > 0) {
            carga = CargaService.getById(TrataID.IntegerToInteger(Ids.getIdCarga()));

        } else {
            carga = new Carga();
        }

        carga.setFd(Numero.stringToDouble(this.campoFd.getText(), 1));
        carga.setFu(Numero.stringToDouble(this.campoFutil.getText(), 1));
        carga.setLocalizacao(this.campoLocal.getText());
        carga.setDescricao(this.textoDescricao.getText());
        carga.setUsabilidade((Usabilidade) cbTipo.getModel().getSelectedItem());
        carga.setQuantidade(Numero.stringToInteger(campoQuantidade.getText(), 1));
        carga.setRendimento(Numero.stringToDouble(this.campoRendimento.getText(), 1));
        carga.setfSimu(Numero.stringToDouble(this.campoSimutaneidade.getText(), 1));
        carga.setFs(Numero.stringToDouble(this.campoFServico.getText(), 1));
        carga.setLigacao((Ligacao) cbLigacao.getModel().getSelectedItem());
        carga.setNome(this.campoNome.getText());
        carga.setnPolos(Numero.stringToInteger(campoNPolos.getText(), 1));
        carga.setPerdasReator(Numero.stringToDouble(this.campoPerdas.getText(), 1));
        carga.setPotencia(Numero.stringToDouble(this.campoPotencia.getText(), 1));
        carga.setRendimento(Numero.stringToDouble(this.campoRendimento.getText(), 1));
        carga.setFp(Numero.stringToDouble(this.campoFPotencia.getText(), 1));
        carga.setUnidade((UnidadePotencia) cbUnidade.getModel().getSelectedItem());
        carga.setComprimentoInstal(Numero.stringToDouble(this.campoComprimento.getText(), 0));

        return carga;
    }

